package metotlar_siniflar_diziler_OOP.FinalProject;

public class Player {
    public int chooseX;
    public int chooseY;

    public Player() {
        this.chooseX = chooseX;
        this.chooseY = chooseY;
    }

    public int getChooseX() {
        return chooseX;
    }

    public void setChooseX(int chooseX) {
        this.chooseX = chooseX;
    }

    public int getChooseY() {
        return chooseY;
    }

    public void setChooseY(int chooseY) {
        this.chooseY = chooseY;
    }


}
